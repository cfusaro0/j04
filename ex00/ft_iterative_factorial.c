int		ft_iterative_factorial(int nb)
{
	int n;
	int o;

	n = 1;
	o = 1;
	if (nb < 0)
		return (0);
	if ((nb == 0) || (nb == 1))
		return (1);
	while (n <= nb)
	{
		o = o * n;
		n++;
	}
	return (o);
}
